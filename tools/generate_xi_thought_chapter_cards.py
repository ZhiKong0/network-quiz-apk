def strip_prefix(knowledge: str) -> str:
    if "：" in knowledge:
        return knowledge.split("：", 1)[1].strip()
    return knowledge.strip()


def type_distribution(items):
    counts = Counter(q["typeName"] for q in items)
    order = ["单选题", "多选题", "判断题", "填空题", "大题"]
    return "；".join(f"{name} {counts[name]}" for name in order if counts.get(name))


def short_answer(q):
    answer = q.get("answer")
    if isinstance(answer, list):
        return "、".join(str(x) for x in answer)
    return str(answer or "").replace("\n", " ").strip()


def build_question_tips(items, limit=16):
    tips = []
    seen = set()
    essays = [q for q in items if q.get("type") == "essay"]
    others = [q for q in items if q.get("type") != "essay"]
    for q in essays + others:
        key = strip_prefix(q.get("knowledge", ""))
        if key in seen:
            continue
        seen.add(key)
        answer = short_answer(q)
        if len(answer) > 40:
            answer = answer[:40] + "…"
        tip = f"{q['label']}：{key}"
        if answer:
            tip += f"；答案要点 {answer}"
        tips.append(tip)
        if len(tips) >= limit:
            break
    return tips


def build_markdown(chapter, guide, labels, type_text):
    def bullets(values):
        return "\n".join(f"- {v}" for v in values)

    front = (
        f"# {chapter} 知识点导图\n\n"
        f"## 主线定位（总览）\n\n"
        f"- {guide['chapterMap']}\n"
        f"- 题型覆盖：{type_text}\n"
        f"- 覆盖题号：{'、'.join(labels[:30])}{'……' if len(labels) > 30 else ''}\n\n"
        f"## 核心知识点（先理解）\n\n{bullets(guide['corePoints'])}\n\n"
        f"## 必背标准词（直接拿分）\n\n{bullets(guide['mustRemember'])}\n"
    )
    back = (
        f"# {chapter} 易混辨析\n\n"
        f"## 易错辨析（防止混概念）\n\n{bullets(guide['traps'])}\n\n"
        f"## 题眼线索（看到就定位）\n\n{bullets(guide['eyeLines'])}\n\n"
        f"## 自测问题（闭眼复述）\n\n{bullets(guide['selfChecks'])}\n"
    )
    return front, back


def node(title, summary="", badge="", points=None, children=None):
    return {
        "title": title,
        "summary": summary,
        "badge": badge,
        "points": points or [],
        "children": children or [],
    }


def build_nodes(guide, tips, labels):
    return [
        node("主线定位", guide["chapterMap"], "总览", [guide["layerHint"]]),
        node("核心知识点", "先把这些逻辑吃透，再回到题目判断。", "理解", guide["corePoints"]),
        node("必背标准词", "选择题和大题最容易直接考这些固定表述。", "拿分", guide["mustRemember"]),
        node("易混辨析", "这些是相近概念最容易被题干偷换的地方。", "防错", guide["traps"]),
        node("题眼线索", "看到这些关键词，就能快速定位本讲知识块。", "定位", guide["eyeLines"]),
        node("逐题覆盖", "代表题线索；完整题号已覆盖在本导图卡片中。", f"{len(labels)}题", tips),
    ]


def main():
    questions = json.loads(QUESTIONS.read_text(encoding="utf-8"))
    by_chapter = OrderedDict()
    for q in questions:
        by_chapter.setdefault(q["chapter"], []).append(q)

    missing = [chapter for chapter in by_chapter if chapter not in GUIDES]
    if missing:
        raise SystemExit(f"Missing guide definitions: {missing}")

    cards = []
    for chapter, items in by_chapter.items():
        guide = GUIDES[chapter]
        labels = [q["label"] for q in items]
        type_text = type_distribution(items)
        tips = build_question_tips(items)
        front, back = build_markdown(chapter, guide, labels, type_text)
        card = {
            "chapter": chapter,
            "knowledge": guide["knowledge"],
            "questionCount": len(items),
            "typeDistribution": type_text,
            "labels": labels,
            "layerHint": guide["layerHint"],
            "chapterMap": guide["chapterMap"],
            "eyeLines": guide["eyeLines"],
            "selfChecks": guide["selfChecks"],
            "corePoints": guide["corePoints"],
            "mustRemember": guide["mustRemember"],
            "traps": guide["traps"],
            "questionTips": tips,
            "frontMarkdown": front,
            "backMarkdown": back,
            "mindMapTitle": guide["knowledge"],
            "mindMapNodes": build_nodes(guide, tips, labels),
        }
        cards.append(card)

    OUT.write_text(json.dumps(cards, ensure_ascii=False, indent=2), encoding="utf-8")
    OVERRIDES.write_text("[]\n", encoding="utf-8")
    print(json.dumps({
        "cards": len(cards),
        "questions": sum(c["questionCount"] for c in cards),
        "out": str(OUT),
        "overrides": str(OVERRIDES),
    }, ensure_ascii=False, indent=2))


if __name__ == "__main__":
    main()
